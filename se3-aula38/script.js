//OPERADORES LÓGICOS
/*
&& - E (AND)
|| - OU (OR)
! - NÃO (NOT)
*/
console.log('Expressão && (AND - E)')
console.log(`True && False =  ${true && false}`)
console.log(`True && True =  ${true && true}`)
console.log(`False && False =  ${false && false}`)
console.log(`False && True =  ${false && true}`)
console.log('')
console.log('Expressão || (OR - OU)')
console.log(`True || False =  ${true || false}`)
console.log(`True || True =  ${true || true}`)
console.log(`False || False =  ${false || false}`)
console.log(`False || True =  ${false || true}`)
console.log('')
console.log('Expressão ! (NOT - NÃO)')
console.log(`Não é verdadeiro = ${!true}`)
console.log(`Não é falso =  ${!false}`)

